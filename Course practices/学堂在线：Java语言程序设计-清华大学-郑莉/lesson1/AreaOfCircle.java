import java.util.*;
import java.io.*;

public class AreaOfCircle
{
    public static void main(String[] args)
    {
        double pi = 3.1415926;
        double r = 15.0;
        double area = pi * r * r;

        System.out.println("半径是"+r+"的圆的面积是"+area);
    }
}


