#include <iostream>
#include <cstdio>
#include <string>
#include <vector>

using namespace std;

int main()
{
    cout << "sizeof(bool) is " << sizeof(bool) << endl;
    cout << "sizeof(char) is " << sizeof(char) << endl;
    cout << "sizeof(wchar_t) is " << sizeof(wchar_t) << endl;
    cout << "sizeof(char16_t) is " << sizeof(char16_t) << endl;
    cout << "sizeof(char32_t) is " << sizeof(char32_t) << endl;
    cout << "sizeof(short) is " << sizeof(short) << endl;
    cout << "sizeof(unsigned short) is " << sizeof(unsigned short) << endl;
    cout << "sizeof(int) is " << sizeof(int) << endl;
    cout << "sizeof(unsigned) is " << sizeof(unsigned) << endl;
    cout << "sizeof(long) is " << sizeof(long) << endl;
    cout << "sizeof(unsigned long) is " << sizeof(unsigned long) << endl;
    cout << "sizeof(long long) is " << sizeof(long long) << endl;
    cout << "sizeof(unsigned long long) is " << sizeof(unsigned long long) << endl;
    cout << "sizeof(float) is " << sizeof(float) << endl;
    cout << "sizeof(double) is " << sizeof(double) << endl;
    cout << "sizeof(long double) is " << sizeof(long double) << endl;
    return 0;
}

