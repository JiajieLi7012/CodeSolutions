#求100~999之间的水仙花数
#Coding 酸饺子

print('-------------酸饺子学Python-------------')

print('100~999之间的所有的水仙花数有：')
for num in range(100,1000):
    if (num//100) ** 3 + ((num - (num//100)*100)//10 ) ** 3\
       + (num - (num//100)*100-((num - (num//100)*100)//10 )*10) ** 3 == num:
        print(num, end = ' ')


